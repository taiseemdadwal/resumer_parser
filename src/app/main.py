def _skills_focused_text(prompt: str) -> str:
    """Prefer text near skill-oriented headings before falling back to full prompt text.

    This helper biases extraction toward the section most likely to list technologies.

    Args:
        prompt: Full skills extraction prompt.

    Returns:
        str: Skill-focused text segment when detected, otherwise full prompt.

    Raises:
        None.
    """

    patterns = (
        r"(skill set.*?)(professional experience|experience|education|$)",
        r"(technical skills.*?)(experience|education|$)",
        r"(skills.*?)(experience|education|$)",
    )
    for pattern in patterns:
        match = re.search(pattern, prompt, flags=re.IGNORECASE | re.DOTALL)
        if match:
            return match.group(1)
    return prompt


class _StubSkillsLLMClient(LLMClient):
    """Minimal stub LLM client that returns an empty skills array."""

    def complete(self, prompt: str) -> str:
        """Return an empty JSON array for skill extraction.

        This keeps the skills field deterministic when LLM extraction is disabled.

        Args:
            prompt: Prompt text generated by ``SkillsExtractor``.

        Returns:
            str: Empty JSON array payload.

        Raises:
            None.
        """

        _ = prompt
        return "[]"


